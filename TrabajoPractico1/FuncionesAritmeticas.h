#include <stdio.h>

/** \brief nos facilita restar los valores ingresados
 *
 * \param double primerValor
 * \param double segundoValor
 * \return double resultado
 *
 */
double restarNumeros(double, double);

/** \brief se encarga de sumar los valores ingresados.
 *
 * \param double primerValor
 * \param double segundoValor
 * \return double resultado
 *
 */
double sumarNumeros(double, double);

/** \brief nos permite dividir los valores ingresados
 *
 * \param primerValor
 * \param segundoValor
 * \return resultado
 *
 */
double dividirNumeros (double, double);

/** \brief permite la multiplicacion de los valores ingresados
 *
 * \param primerValor
 * \param segundoValor
 * \return resultado
 *
 */
double multiplicarNumeros (double, double);

/** \brief se encarga de calcular el factorial del valor ingresado
 *
 * \param double primerValor
 * \return double resultado
 *
 */
int calcularFactorial(int);

